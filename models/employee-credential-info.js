const mongoose = require('mongoose');

const employeeCredentialInfoSchema = new mongoose.Schema({
    firstname: {
        type: String,
        required: true,
        trim: true
    },
    lastname: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    confirmPassword: {
        type: String,
        required: false
    },
  
}, { timestamps: true });

module.exports = mongoose.model('employee_credential_info',employeeCredentialInfoSchema);